# Programa en Python equivalente a MemoriaDinamica (con ArrayList en Java)

# Creando una lista de tipo String
frutas = []

# Agregando elementos a la lista
frutas.append("Mango")
frutas.append("Manzana")
frutas.append("Banana")
frutas.append("Uvas")

# Imprimiendo la lista completa
print(frutas)

# Eliminando elementos por su índice
frutas.pop(0)  # Elimina "Mango"
frutas.pop(1)  # Elimina "Banana" (ahora está en el índice 1)

# Agregando un nuevo elemento al final
frutas.append("Sandia")

# Imprimiendo la lista final
print(frutas)
